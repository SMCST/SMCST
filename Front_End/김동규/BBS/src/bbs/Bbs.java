package bbs;

public class Bbs {
	private int bbsBoardNumber;
	private String bbsAcademicNumber;
	private String bbsID;
	private String bbsTitle;
	private String bbsContents;
	private String bbsDate;
	private int bbsCode;
	private int bbsViews;
	
	public int getBbsBoardNumber() {
		return bbsBoardNumber;
	}
	public void setBbsBoardNumber(int bbsBoardNumber) {
		this.bbsBoardNumber = bbsBoardNumber;
	}
	public String getBbsAcademicNumber() {
		return bbsAcademicNumber;
	}
	public void setBbsAcademicNumber(String bbsAcademicNumber) {
		this.bbsAcademicNumber = bbsAcademicNumber;
	}
	public String getBbsID() {
		return bbsID;
	}
	public void setBbsID(String bbsID) {
		this.bbsID = bbsID;
	}
	public String getBbsTitle() {
		return bbsTitle;
	}
	public void setBbsTitle(String bbsTitle) {
		this.bbsTitle = bbsTitle;
	}
	public String getBbsContents() {
		return bbsContents;
	}
	public void setBbsContents(String bbsContents) {
		this.bbsContents = bbsContents;
	}
	public String getBbsDate() {
		return bbsDate;
	}
	public void setBbsDate(String bbsDate) {
		this.bbsDate = bbsDate;
	}
	public int getBbsCode() {
		return bbsCode;
	}
	public void setBbsCode(int bbsCode) {
		this.bbsCode = bbsCode;
	}
	public int getBbsViews() {
		return bbsViews;
	}
	public void setBbsViews(int bbsViews) {
		this.bbsViews = bbsViews;
	}
}
